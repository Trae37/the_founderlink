const normalizeResponseFormat = ({
  responseFormat,
  response_format,
  outputSchema,
  output_schema,
}: {
  responseFormat?: ResponseFormat;
  response_format?: ResponseFormat;
  outputSchema?: OutputSchema;
  output_schema?: OutputSchema;
}):
  | { type: "json_schema"; json_schema: JsonSchema }
  | { type: "text" }
  | { type: "json_object" }
  | undefined => {
  const explicitFormat = responseFormat || response_format;
  if (explicitFormat) {
    if (
      explicitFormat.type === "json_schema" &&
      !explicitFormat.json_schema?.schema
    ) {
      throw new Error(
        "responseFormat json_schema requires a defined schema object"
      );
    }
    return explicitFormat;
  }

  const schema = outputSchema || output_schema;
  if (!schema) return undefined;

  if (!schema.name || !schema.schema) {
    throw new Error("outputSchema requires both name and schema");
  }

  return {
    type: "json_schema",
    json_schema: {
      name: schema.name,
      schema: schema.schema,
      ...(typeof schema.strict === "boolean" ? { strict: schema.strict } : {}),
    },
  };
};

let anthropicClient: Anthropic | null = null;

function getAnthropicClient(): Anthropic {
  const apiKey = process.env.ANTHROPIC_API_KEY;
  if (!apiKey) {
    throw new Error("ANTHROPIC_API_KEY is not configured");
  }
  if (!anthropicClient) {
    anthropicClient = new Anthropic({ apiKey, timeout: 30000 });
  }
  return anthropicClient;
}

export async function invokeLLM(params: InvokeParams): Promise<InvokeResult> {
  const { messages } = params;

  // Convert messages to Anthropic format
  const systemMessage = messages.find(m => m.role === "system");
  const nonSystemMessages = messages.filter(m => m.role !== "system");

  const anthropicMessages = nonSystemMessages.map(m => ({
    role: m.role === "assistant" ? "assistant" as const : "user" as const,
    content: typeof m.content === "string" ? m.content : JSON.stringify(m.content),
  }));

  const client = getAnthropicClient();
  const response = await client.messages.create({
    model: "claude-sonnet-4-20250514",
    max_tokens: 2000,
    system: systemMessage ? (typeof systemMessage.content === "string" ? systemMessage.content : JSON.stringify(systemMessage.content)) : undefined,
    messages: anthropicMessages,
  });

  const textBlock = response.content.find(block => block.type === "text");
  const content = textBlock?.text || "";

  // Return in OpenAI-compatible format for existing code
  return {
    id: response.id,
    created: Date.now(),
    model: response.model,
    choices: [{
      index: 0,
      message: {
        role: "assistant",
        content,
      },
      finish_reason: response.stop_reason || "stop",
    }],
    usage: {
      prompt_tokens: response.usage.input_tokens,
      completion_tokens: response.usage.output_tokens,
      total_tokens: response.usage.input_tokens + response.usage.output_tokens,
    },
  };
}
